package JavaTutorial;

public class SwapNumber {

	public static void main(String[] args) {
		int number1 = 10;
		int number2 = 20;
		number1 = number1 + number2;
		number2 = number1- number2;
		number1 = number1 - number2;
		System.out.println("the swapped values are"+ ' '+number1 +' '+ +number2);
		

	}

}
