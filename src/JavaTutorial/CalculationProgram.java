package JavaTutorial;

public class CalculationProgram {

	public static void main(String[] args) {
		int number1 = 40;
		int number2 = 20;
		int sum, substraction, product, division;
	    sum = number1 + number2;
	    substraction = number1 - number2;
	    product = number1 * number2;
	    division = number1 / number2;
	    System.out.println("the sum of number1 and number2 is"+ ' '+sum);
	    System.out.println("the substraction of number1 and number2 is"+ ' '+substraction);
	    System.out.println("the product of number1 and number2 is"+ ' '+product);
	    System.out.println("the division of number1 and number2 is"+ ' '+division);
	    
		

	}

}
